p1 = input("Insira o seu nome: ")
a1 = float(input("Insira a sua altura: "))
p2 = input("Insira o nome da outra pessoa: ")
a2 = float(input("Insira a altura da outra pessoa: "))

if a1 > a2:
    print(f"A altura de {p1} é maior que a altura de {p2}.")
else:
    print(f"A altura de {p1} é menor que a altura de {p2}.")
